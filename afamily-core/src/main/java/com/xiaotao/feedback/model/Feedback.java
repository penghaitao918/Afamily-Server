public class Feedback  extends PageBase {

    private int id = 0;
    private String name = null;
    private String title = null;
    private String body = null;
    private String time = "";

    public Feedback(){ }

    public Feedback(JSONObject jsonObject) {
        try {
            this.name = jsonObject.getString(info.who);
            this.title = jsonObject.getString(info.title);
            this.body = jsonObject.getString(info.body);
        } catch (JSONException e) {
            e.printStackTrace();
        }
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    //  info
    public static final class info {
        public static final String who = "FEEDBACK_NAME";
        public static final String title = "FEEDBACK_TITLE";
        public static final String body = "FEEDBACK_BODY";
    }

}
